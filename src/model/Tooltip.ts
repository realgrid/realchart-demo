import { ChartItem } from "./ChartItem";
import { DataPoint } from "./DataPoint";
import { ISeries } from "./Series";

export class Tooltip extends ChartItem {

    //-------------------------------------------------------------------------
    // property fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // constructor
    //-------------------------------------------------------------------------
    constructor(public series: ISeries) {
        super(series.chart);
    }

    //-------------------------------------------------------------------------
    // properties
    //-------------------------------------------------------------------------
    html: string;
    text: string;

    //-------------------------------------------------------------------------
    // methods
    //-------------------------------------------------------------------------
    getValue(point: DataPoint, param: string): string {
        let v: any;

        switch (param) {
            case 'series.name':
                break;
            case 'point.xValue':
                break;
            case 'point.yValue':
                break;
            default:
                break;
        }
        return v;
    }

    //-------------------------------------------------------------------------
    // overriden members
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // internal members
    //-------------------------------------------------------------------------
}