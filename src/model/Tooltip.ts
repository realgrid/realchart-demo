import { ChartItem } from "./ChartItem";
import { DataPoint } from "./DataPoint";
import { ISeries } from "./Series";

export class Tooltip extends ChartItem {

    //-------------------------------------------------------------------------
    // consts
    //-------------------------------------------------------------------------
    static readonly HIDE_DELAY = 1000;

    //-------------------------------------------------------------------------
    // property fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // constructor
    //-------------------------------------------------------------------------
    constructor(public series: ISeries) {
        super(series.chart);
    }

    //-------------------------------------------------------------------------
    // properties
    //-------------------------------------------------------------------------
    html: string;
    text = '<b>${point.x}</b><br>${series}:<b> ${point.y}</b>';
    offset = 8;
    hideDelay = Tooltip.HIDE_DELAY;
    minWidth = 100;
    minHeight = 40;

    //-------------------------------------------------------------------------
    // methods
    //-------------------------------------------------------------------------
    getValue(point: DataPoint, param: string): string {
        switch (param) {
            case 'series':
            case 'series.name':
                return this.series.displayName();
            case 'point.x':
                return point.x;
            case 'point':
            case 'point.y':
                return point.y;
            default:
                return param;
        }
    }

    //-------------------------------------------------------------------------
    // overriden members
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // internal members
    //-------------------------------------------------------------------------
}