import { ChartItem } from "./ChartItem";

export abstract class PlotItem extends ChartItem {

    //-------------------------------------------------------------------------
    // properties
    //-------------------------------------------------------------------------
    left: number;
    right: number;
    top: number;
    bottom: number;
    front = true;

    //-------------------------------------------------------------------------
    // overriden members
    //-------------------------------------------------------------------------
}

export class TextItem extends PlotItem {

    //-------------------------------------------------------------------------
    // property fields
    //-------------------------------------------------------------------------
    text: string;
}

export class ImageItem extends PlotItem {

    //-------------------------------------------------------------------------
    // property fields
    //-------------------------------------------------------------------------
    url: string;
    width: number;
    height: number;
}