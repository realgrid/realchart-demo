import { RcElement } from "../RcControl";

export class CircleElement extends RcElement {

    //-------------------------------------------------------------------------
    // consts
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // static members
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // constructors
    //-------------------------------------------------------------------------
    constructor(doc: Document, cx?: number, cy?: number, radius?: number, styleName = '') {
        super(doc, 'circle', styleName);

        if (typeof cx === 'number') {
            this.setCircle(cx, cy, radius);
        }
    }


	//-------------------------------------------------------------------------
    // properties
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // methods
    //-------------------------------------------------------------------------
    setCircle(cx: number, cy: number, radius: number) {
        this.setAttrs({
            cx: cx, 
            cy: cy,
            r: radius
        });
    }

    //-------------------------------------------------------------------------
    // overriden members
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // internal members
    //-------------------------------------------------------------------------
}
