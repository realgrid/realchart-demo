import { RcElement } from '../RcControl';
import { _undefined } from '../Types';

export abstract class GroupElement extends RcElement {

    //-------------------------------------------------------------------------
    // consts
    //-------------------------------------------------------------------------
    private static IGNORE_ATTRS = {
        width: '',
        height: ''
    }

    //-------------------------------------------------------------------------
    // static members
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // fields
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // constructor
    //-------------------------------------------------------------------------
    constructor(doc: Document, styleName: string = _undefined) {
        super(doc, styleName, 'g');

        this._doInitChildren(doc);
    }

	//-------------------------------------------------------------------------
    // properties
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // methods
    //-------------------------------------------------------------------------
    //-------------------------------------------------------------------------
    // overriden members
    //-------------------------------------------------------------------------
    protected _movable(): boolean {
        return false;
    }

    setAttr(attr: string, value: any): RcElement {
        if (!GroupElement.IGNORE_ATTRS.hasOwnProperty(attr)) {
            super.setAttr(attr, value);
        }
        return this;
    }

    //-------------------------------------------------------------------------
    // internal members
    //-------------------------------------------------------------------------
    protected _doInitChildren(doc: Document): void {
    }
}
